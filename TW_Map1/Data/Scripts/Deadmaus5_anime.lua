local Ease3D = require(script:GetCustomProperty("Ease3D"))
local root = script.parent
local Head = script:GetCustomProperty("head"):WaitForObject()
local rotation1 = script:GetCustomProperty("rotation1")
local rotation2 = script:GetCustomProperty("rotation2")
local rotation3 = script:GetCustomProperty("rotation3")
local transform1 = script:GetCustomProperty("transform1")
local transform2 = script:GetCustomProperty("transform2")
local transform3 = script:GetCustomProperty("transform3")

function Tick()
    Ease3D.EaseRotation(Head, rotation1, 0.6, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.OUT)
    Ease3D.EasePosition(Head, transform1, 0.6, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.INOUT)
    Task.Wait(0.6)
    Ease3D.EaseRotation(Head, rotation3, 0.4, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.IN)
    Ease3D.EasePosition(Head, transform3, 0.4, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.INOUT)
    Task.Wait(0.4)
    Ease3D.EaseRotation(Head, rotation2, 0.6, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.OUT)
    Ease3D.EasePosition(Head, transform2, 0.6, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.INOUT)
    Task.Wait(0.6)
    Ease3D.EaseRotation(Head, rotation3, 0.4, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.IN)
    Ease3D.EasePosition(Head, transform3, 0.4, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.INOUT)
    Task.Wait(0.4)
end