---- HANDLING TOGGLE DROPDOWN MENU
-- TOGGLE BOTH
function TOGGLE_DROPDOWN()
	if CHOOSE_MODE.y == CLOSED_DROPDOWN_Y then
	CHOOSE_MODE_DROPDOWN_ARROW_OPEN.visibility = Visibility.FORCE_OFF
	CHOOSE_MODE_DROPDOWN_ARROW_CLOSE.visibility = Visibility.FORCE_ON
	SFX_SLIDE_DOWN:Play()
		if isMoving and timeRemaining > 0 then
			targetDestination = OPEN_DROPDOWN_Y
			timeRemaining = DROPDOWN_TOGGLE_TIME - timeRemaining
		else
			isMoving = true
			targetDestination = OPEN_DROPDOWN_Y
			timeRemaining = DROPDOWN_TOGGLE_TIME
		end
	else
	CHOOSE_MODE_DROPDOWN_ARROW_OPEN.visibility = Visibility.FORCE_ON
	CHOOSE_MODE_DROPDOWN_ARROW_CLOSE.visibility = Visibility.FORCE_OFF
	SFX_SLIDE_UP:Play()
		if isMoving and timeRemaining > 0 then
			targetDestination = CLOSED_DROPDOWN_Y
			timeRemaining = DROPDOWN_TOGGLE_TIME - timeRemaining
		else
			isMoving = true
			targetDestination = CLOSED_DROPDOWN_Y
			timeRemaining = DROPDOWN_TOGGLE_TIME
		end
	end
end

-- TOGGLE STATS
function STATS_TOGGLE_DROPDOWN()
	if STATS_CONTAINER.x == STATS_CLOSED_DROPDOWN_X then
	SFX_SLIDE_DOWN:Play()
		if isMoving and timeRemaining > 0 then
			STATS_targetDestination = STATS_OPEN_DROPDOWN_X
			STATS_timeRemaining = STATS_DROPDOWN_TOGGLE_TIME - STATS_timeRemaining
		else
			STATS_isMoving = true
			STATS_targetDestination = STATS_OPEN_DROPDOWN_X
			STATS_timeRemaining = STATS_DROPDOWN_TOGGLE_TIME
		end
	Task.Wait(0.1)
	STATS_DROPDOWN_ARROW_OPEN.visibility = Visibility.FORCE_OFF
	STATS_DROPDOWN_ARROW_CLOSE.visibility = Visibility.FORCE_ON
	else
	SFX_SLIDE_UP:Play()
		if STATS_isMoving and STATS_timeRemaining > 0 then
			STATS_targetDestination = STATS_CLOSED_DROPDOWN_X
			STATS_timeRemaining = STATS_DROPDOWN_TOGGLE_TIME - STATS_timeRemaining
		else
			STATS_isMoving = true
			STATS_targetDestination = STATS_CLOSED_DROPDOWN_X
			STATS_timeRemaining = STATS_DROPDOWN_TOGGLE_TIME
		end
	Task.Wait(0.1)
	STATS_DROPDOWN_ARROW_OPEN.visibility = Visibility.FORCE_ON
	STATS_DROPDOWN_ARROW_CLOSE.visibility = Visibility.FORCE_OFF
	end
end

